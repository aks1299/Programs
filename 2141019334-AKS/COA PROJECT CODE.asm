ASSUME CS:CODE, DS:DATA

DATA SEGMENT
    STRING DB 'Hello, World$'
    PATTERN DB 'World$'
    STR_LEN DW $-STRING
    PAT_LEN DW $-PATTERN
    FOUND DB 0
DATA ENDS

CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX
    
    LEA SI, STRING
    LEA DI, PATTERN
    
    MOV CX, STR_LEN
    SUB CX, PAT_LEN
    
SEARCH_LOOP:
    MOV BX, CX
    MOV DX, DI
    
INNER_LOOP:
    MOV AL, [SI]
    MOV BL, [DI]
    
    CMP AL, BL
    JNE NOT_FOUND
    
    INC SI
    INC DI
    
    CMP DI, DX
    JE PATTERN_FOUND
    
    LOOP INNER_LOOP
    
    MOV DI, DX
    INC CX
    LOOP SEARCH_LOOP
    
NOT_FOUND:
    MOV FOUND, 0
    JMP EXIT
    
PATTERN_FOUND:
    MOV FOUND, 1
    
EXIT:
    MOV AH, 4Ch
    INT 21h
CODE ENDS

END START